# Example 1: Short hand if
a = 200
b = 33
print("A") if a > b else print("B")

# Example 2: Short hand if else
x = 5
y = 10
print("x") if x > y else print("y")

# Example 3: Short hand condition
print("Yes") if 5 > 2 else print("No")

# Example 4: Short hand equality
a = 10
b = 10
print("Equal") if a == b else print("Not equal")

# Example 5: Short hand boolean
print("True") if True else print("False")
