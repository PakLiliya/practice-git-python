# Example 1: if else statement
a = 33
b = 200
if b > a:
    print("b is greater than a")
else:
    print("b is not greater than a")

# Example 2: if else comparison
x = 5
y = 10
if x > y:
    print("x is greater than y")
else:
    print("y is greater than x")

# Example 3: if else with false condition
if 10 > 20:
    print("Yes")
else:
    print("No")

# Example 4: if else with boolean
if False:
    print("True")
else:
    print("False")

# Example 5: if else with equal values
a = 50
b = 50
if a > b:
    print("a is greater")
else:
    print("a is not greater")
