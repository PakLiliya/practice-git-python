# Example 1: if elif statement
a = 33
b = 33
if b > a:
    print("b is greater than a")
elif a == b:
    print("a and b are equal")

# Example 2: if elif else statement
x = 10
y = 10
if x > y:
    print("x > y")
elif x == y:
    print("x == y")
else:
    print("x < y")

# Example 3: multiple conditions
a = 200
b = 33
if b > a:
    print("b > a")
elif a > b:
    print("a > b")
else:
    print("equal")

# Example 4: number comparison
num = 0
if num > 0:
    print("positive")
elif num == 0:
    print("zero")
else:
    print("negative")

# Example 5: age check
