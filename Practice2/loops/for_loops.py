# Example 1: for loop through list
fruits = ["apple", "banana", "cherry"]
for x in fruits:
    print(x)

# Example 2: for loop through string
for x in "banana":
    print(x)

# Example 3: range loop
for x in range(6):
    print(x)

# Example 4: range with start and stop
for x in range(2, 6):
    print(x)

# Example 5: range with step
for x in range(2, 30, 3):
    print(x)
