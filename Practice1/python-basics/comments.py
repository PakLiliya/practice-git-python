# Пример 1: Однострочный комментарий
# This is a comment
print("Hello, World!")
# Строка с # не выполняется

# Пример 2: Комментарий в конце строки
print("Hello, World!") # This is a comment
# Код выполняется, комментарий после # игнорируется

# Пример 3: Закомментированный код
# print("Hello, World!")
print("Cheers, Mate!")
# Первая команда print не выполняется

# Пример 4: Многострочный комментарий из отдельных строк
# This is a comment
# written in
# more than just one line
print("Hello, World!")
# Несколько строк, каждая начинается с #

# Пример 5: Многострочный комментарий с тройными кавычками
"""
This is a comment
written in
more than just one line
"""
print("Hello, World!")
# Текст между тройными кавычками игнорируется
