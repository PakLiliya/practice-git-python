# Пример 1: Преобразование в целое число
x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3
# int() преобразует в целое, отбрасывая дробную часть

# Пример 2: Преобразование в float
x = float(1)     # x will be 1.0
y = float(2.8)   # y will be 2.8
z = float("3")   # z will be 3.0
w = float("4.2") # w will be 4.2
# float() преобразует в число с плавающей точкой

# Пример 3: Преобразование в строку
x = str("s1") # x will be 's1'
y = str(2)    # y will be '2'
z = str(3.0)  # z will be '3.0'
# str() преобразует любое значение в строку

# Пример 4: Разные варианты преобразования
x = int("3") # Работает
