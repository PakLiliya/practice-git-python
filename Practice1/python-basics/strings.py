# Пример 1: Создание строк
print("Hello")
print('Hello')
# Строки можно создавать с двойными или одинарными кавычками

# Пример 2: Присвоение строки переменной
a = "Hello"
print(a)
# Строка сохраняется в переменной a, затем выводится

# Пример 3: Многострочная строка
a = """Lorem ipsum dolor sit amet,
consectetur adipiscing elit,
sed do eiusmod tempor incididunt
ut labore et dolore magna aliqua."""
print(a)
# Тройные кавычки позволяют создавать многострочные строки

# Пример 4: Доступ к символам по индексу
a = "Hello, World!"
print(a[1])
# Выводит символ с индексом 1 (второй символ, 'e')

# Пример 5: Срез строки
b = "Hello, World!"
print(b[2:5])
# Выводит символы с индекса 2 по 4 (не включая 5) - 'llo'

# Пример 6: Отрицательная индексация
b = "Hello, World!"
print(b[-5:-2])
# Отрицательные индексы отсчитывают с конца строки (-5:-2 = 'orl')
