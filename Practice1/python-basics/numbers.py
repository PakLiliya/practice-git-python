# Пример 1: Три типа чисел
x = 1    # int
y = 2.8  # float
z = 1j   # complex
# int - целые, float - с плавающей точкой, complex - комплексные

# Пример 2: Целые числа разных размеров
x = 1
y = 35656222554887711
z = -3255522
# Целые числа могут быть очень большими или отрицательными

# Пример 3: Числа с плавающей точкой
x = 1.10
y = 1.0
z = -35.59
# Числа с десятичной точкой или отрицательные

# Пример 4: Научная нотация
x = 35e3
y = 12E4
z = -87.7e100
# e или E обозначает степень 10 (35e3 = 35 × 10³ = 35000)

# Пример 5: Комплексные числа
x = 3+5j
y = 5j
z = -5j
# Комплексные числа с мнимой частью j
