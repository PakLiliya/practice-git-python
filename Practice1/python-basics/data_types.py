# Пример 1: Целое число (int)
x = 5
print(type(x))
# Вывод: <class 'int'> - целочисленный тип

# Пример 2: Строка (str)
x = "Hello World"
print(type(x))
# Вывод: <class 'str'> - строковый тип

# Пример 3: Число с плавающей точкой (float)
x = 20.5
print(type(x))
# Вывод: <class 'float'> - вещественное число

# Пример 4: Список (list)
x = ["apple", "banana", "cherry"]
print(type(x))
# Вывод: <class 'list'> - изменяемая коллекция

# Пример 5: Словарь (dict)
x = {"name": "John", "age": 36}
print(type(x))
# Вывод: <class 'dict'> - словарь (пары ключ-значение)

# Пример 6: Логическое значение (bool)
x = True
print(type(x))
# Вывод: <class 'bool'> - логический тип (True/False)
