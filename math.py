import math
import random


# 1. Built-in min and max

x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)


# 2. abs and pow

x = abs(-7.25)
y = pow(4, 3)

print(x)
print(y)


# 3. math.sqrt

x = math.sqrt(64)
print(x)


# 4. math.ceil and math.floor

x = math.ceil(1.4)
y = math.floor(1.4)

print(x)
print(y)


# 5. random numbers

print(random.randrange(1, 10))